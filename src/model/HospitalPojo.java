package model;

public class HospitalPojo {
	String hospitalId;
	String hospitalAdd;
	String hospitalState;
	String hospitalMail;
	String hospitalNum;
	String hospitalName;
	public String getHospitalId() {
		return hospitalId;
	}
	public void setHospitalId(String hospitalId) {
		this.hospitalId = hospitalId;
	}
	public String getHospitalAdd() {
		return hospitalAdd;
	}
	public void setHospitalAdd(String hospitalAdd) {
		this.hospitalAdd = hospitalAdd;
	}
	public String getHospitalState() {
		return hospitalState;
	}
	public void setHospitalState(String hospitalState) {
		this.hospitalState = hospitalState;
	}
	public String getHospitalMail() {
		return hospitalMail;
	}
	public void setHospitalMail(String hospitalMail) {
		this.hospitalMail = hospitalMail;
	}
	public String getHospitalNum() {
		return hospitalNum;
	}
	public void setHospitalNum(String hospitalNum) {
		this.hospitalNum = hospitalNum;
	}
	public String getHospitalName() {
		return hospitalName;
	}
	public void setHospitalName(String hospitalName) {
		this.hospitalName = hospitalName;
	}

}
